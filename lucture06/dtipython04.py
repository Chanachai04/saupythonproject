# คำสั่ง if-else-if
score = int(input('กรอกคะแนน :'))

if score >= 80:
    print('ได้เกรด A')
elif score >= 70 & score < 80:
    print('ได้เกรด B')
elif score >= 60 & score < 70:
    print('ได้เกรด C')
elif score >= 50 & score < 60:
    print('ได้เกรด D')
else:
    print('ได้เกรด F')
